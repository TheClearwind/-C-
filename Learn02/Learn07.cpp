#include <stdio.h>
#include<stdlib.h>
#include <conio.h>
#include <ctype.h>
#define PWDLEN 20

void getpwd(char *pwd, int pwdlen);

int main() {
	char pwd[PWDLEN + 1];
	printf("Input password: ");
	getpwd(pwd, PWDLEN);
	printf("The password is: %s\n", pwd);
	system("pause");
	return 0;
}

/**
* 获取用户输入的密码
* @param  pwd     char*  保存密码的内存的首地址
* @param  pwdlen  int    密码的最大长度
**/
void getpwd(char *pwd, int pwdlen) {
	char ch = 0;
	int i = 0;
	while (i<pwdlen) {
		ch = getch();

		if (ch == '\r') {  //回车结束输入
			printf("\n");
			break;
		}

		if (ch == '\b' && i>0) {  //按下删除键
			i--;
			printf("\b \b");
		}
		else if (isprint(ch)) {  //输入可打印字符
			pwd[i] = ch;
			printf("*");
			i++;
		}
	}
	pwd[i] = 0; //\0的ascii码就是0
}